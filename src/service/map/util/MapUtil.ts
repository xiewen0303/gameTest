module map {
	export class MapUtil {
		public constructor() {
		}

		/**
		 * 是否为可以合并的类型
		 */
		public static isMergeType(elementType:number):boolean{
			if(elementType == map.ElementType.cat.valueOf() || elementType == map.ElementType.dog.valueOf()){
				return true;
			}
			return false;
		}
	}
}